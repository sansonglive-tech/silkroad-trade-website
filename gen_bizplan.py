def make_table(headers, data, col_widths=None):
    """Create a formatted table. First item of data is header row."""
    full_data = [headers] + data
    ncols = len(headers)
    nrows = len(full_data)
    table = doc.add_table(rows=nrows, cols=ncols)
    table.alignment = WD_TABLE_ALIGNMENT.CENTER

    # Set column widths
    if col_widths:
        for row in table.rows:
            for i, w in enumerate(col_widths):
                row.cells[i].width = Cm(w)

    for ri, row_data in enumerate(full_data):
        for ci, cell_text in enumerate(row_data):
            cell = table.cell(ri, ci)
            set_cell_borders(cell)
            is_header = ri == 0
            if is_header:
                set_cell_shading(cell, "1F4E79")
                add_cell_text(cell, cell_text, bold=True, color=WHITE)
            elif ri % 2 == 1:
                set_cell_shading(cell, "E8F0FE")
                add_cell_text(cell, cell_text)
            else:
                add_cell_text(cell, cell_text)

    # Add spacing after table
    p = doc.add_paragraph()
    p.paragraph_format.space_before = Pt(2)
    p.paragraph_format.space_after = Pt(6)
    return table

def make_simple_table(headers, data, col_widths=None):
    """Variation without auto-alternating"""
    full_data = [headers] + data
    ncols = len(headers)
    nrows = len(full_data)
    table = doc.add_table(rows=nrows, cols=ncols)
    table.alignment = WD_TABLE_ALIGNMENT.CENTER
    if col_widths:
        for row in table.rows:
            for i, w in enumerate(col_widths):
                row.cells[i].width = Cm(w)
    for ri, row_data in enumerate(full_data):
        for ci, cell_text in enumerate(row_data):
            cell = table.cell(ri, ci)
            set_cell_borders(cell)
            if ri == 0:
                set_cell_shading(cell, "1F4E79")
                add_cell_text(cell, cell_text, bold=True, color=WHITE)
            else:
                add_cell_text(cell, cell_text)
    p = doc.add_paragraph()
    p.paragraph_format.space_before = Pt(2)
    p.paragraph_format.space_after = Pt(6)
    return table


